package vo;

public class IScomb {
	private int i;
	private String str;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public IScomb(int i, String str) {
		super();
		this.i = i;
		this.str = str;
	}
	public IScomb() {}
}
